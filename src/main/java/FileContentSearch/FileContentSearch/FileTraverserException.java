package FileContentSearch.FileContentSearch;

public class FileTraverserException extends Exception{

	private static final long serialVersionUID = -8404017760046339555L;

	public FileTraverserException() {
		super();
	}

	public FileTraverserException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FileTraverserException(String message, Throwable cause) {
		super(message, cause);
	}

	public FileTraverserException(String message) {
		super(message);
	}

	public FileTraverserException(Throwable cause) {
		super(cause);
	}

}
